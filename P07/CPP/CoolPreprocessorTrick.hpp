/*Truqes fixes para o preprocessador, retirados de
https://github.com/pfultz2/Cloak/wiki/C-Preprocessor-tricks,-tips,-and-idioms
Utilizamo-los para definir, basicamente estamos a usar o preprocessador para manter o lexer legivel
CASOS(startchar, leng)
Que vai ser substituido por:
case startchar+0: case startchar+1: (...) case startchar+leng-1:
*/
#define CAT(a, ...) PRIMITIVE_CAT(a, __VA_ARGS__)
#define PRIMITIVE_CAT(a, ...) a ## __VA_ARGS__

#define IIF(c) PRIMITIVE_CAT(IIF_, c)
#define IIF_0(t, ...) __VA_ARGS__
#define IIF_1(t, ...) t

#define COMPL(b) PRIMITIVE_CAT(COMPL_, b)
